public class J09StringToLong 

{    public static void main(String[] args) {

        // Step 1: Creating a string containing numeric characters
        String s = "999999999999";
        System.out.println("String: " + s);

        // Step 2: Converting the string into a long value
        long l = Long.parseLong(s);

        // Step 3: Printing the converted long value
        System.out.println("Long: " + l);
    }
}


    

